package com.privalia.presentation;

public class HelloWorld {
	
	private String hello;
	
	
	public HelloWorld() {
		
	}
	
	public HelloWorld(String hello) {
		this.hello = hello;
	}

	/**
	 * @return the hello
	 */
	public String getHello() {
		return hello;
	}

	/**
	 * @param hello the hello to set
	 */
	public void setHello(String hello) {
		this.hello = hello;
	}
	
}
